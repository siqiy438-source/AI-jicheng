/** 感知加权欧氏距离，暗色像素额外惩罚高饱和候选色（防止黑发映射到深绿色号） */
function colorDistance(r1: number, g1: number, b1: number, r2: number, g2: number, b2: number): number {
  const base = 2 * (r1 - r2) ** 2 + 4 * (g1 - g2) ** 2 + 3 * (b1 - b2) ** 2;

  // 当输入像素很暗时，偏向中性色：避免黑发/暗影被匹配到彩色色号
  const lum = 0.299 * r1 + 0.587 * g1 + 0.114 * b1;
  if (lum < 60) {
    const maxC = Math.max(r2, g2, b2);
    const minC = Math.min(r2, g2, b2);
    const sat = maxC > 0 ? (maxC - minC) / maxC : 0; // 候选色的饱和度 0~1
    const darkFactor = 1 - lum / 60; // 越暗惩罚越重
    return base + sat * 100000 * darkFactor;
  }
  return base;
}

/** 在 MARD 调色板中找最近似的色号 */
export function findNearestMardColor(r: number, g: number, b: number): MardColor {
  let best = MARD_PALETTE[0];
  let bestDist = Infinity;
  for (const color of MARD_PALETTE) {
    const dist = colorDistance(r, g, b, color.rgb[0], color.rgb[1], color.rgb[2]);
    if (dist < bestDist) {
      bestDist = dist;
      best = color;
    }
  }
  return best;
}

/** 把 RGB 转为 CSS hex 字符串 */
export function rgbToHex(rgb: [number, number, number]): string {
  return '#' + rgb.map(v => v.toString(16).padStart(2, '0')).join('');
}

/** 判断颜色是否偏暗，用于决定文字颜色（亮背景→黑字，暗背景→白字） */
export function isDark(rgb: [number, number, number]): boolean {
  const luminance = 0.299 * rgb[0] + 0.587 * rgb[1] + 0.114 * rgb[2];
  return luminance < 140;
}
